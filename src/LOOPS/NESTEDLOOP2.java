package LOOPS;

public class NESTEDLOOP2 {

	public static void main(String[] args) {
           
		for (int i = 1; i<=7; i++) //for loop with open bracket.
		{
		 for (int j = 1; j<=5; j++)//for loop with open bracket. 
		 {
		   System.out.print(" @ ");//horizontal.(SOP close bracket)
	    }
	        System.out.println();//vertical.(after this SOP there is an open and close bracket.)
	   {
	}
}
}
}